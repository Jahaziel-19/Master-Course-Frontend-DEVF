// Datos Number
console.log(typeof 22); 
console.log(typeof 3.14);
console.log(typeof -5);
console.log(typeof 0);

// Datos String
console.log(typeof "Treinta y tres"); 
console.log(typeof 'Hola mundo');

// Datos Boolean
console.log(typeof true); 
console.log(typeof false);

// Datos Undefined
console.log(typeof undefined);

// Datos Null
console.log(typeof null); // Esto es un error en JavaScript, pero se considera un tipo de dato especial

console.log(typeof FALSE);